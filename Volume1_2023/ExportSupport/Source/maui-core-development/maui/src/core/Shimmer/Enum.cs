
namespace Syncfusion.Maui.Shimmer
{
    /// <summary>
    /// Defines the type for the SfShimmer.  
    /// </summary>
    public enum ShimmerType
    {
        /// <summary>
        /// Circle persona effect of SfShimmer.  
        /// </summary>
        CirclePersona,

        /// <summary>
        /// Rectangle persona effect of SfShimmer.  
        /// </summary>
        RectanglePersona,

        /// <summary>
        /// Profile effect of SfShimmer.
        /// </summary>
        Profile,

        /// <summary>
        /// Article effect of SfShimmer .
        /// </summary>
        Article,

        /// <summary>
        /// Video effect of SfShimmer.
        /// </summary>
        Video,

        /// <summary>
        /// Feed effect of SfShimmer. 
        /// </summary>
        Feed,

        /// <summary>
        /// Shopping effect of SfShimmer.
        /// </summary>
        Shopping,

        /// <summary>
        /// Custom view of SfShimmer.
        /// </summary>
        Custom
    }

    /// <summary>
    /// Defines the wave direction for the SfShimmer .
    /// </summary>
    public enum ShimmerWaveDirection
    {
        /// <summary>
        /// Default enum of the animation direction is allows the animation from lefttop to rightbottom.
        /// </summary>
        Default,

        /// <summary>
        /// LeftToRight enum of the animation direction is allows the animation from left to right.
        /// </summary>
        LeftToRight,

        /// <summary>
        /// RightToLeft enum of the animation direction is allows the animation from right to left.
        /// </summary>
        RightToLeft,

        /// <summary>
        /// TopToBottom enum of the animation direction is allows the animation from top to bottom.
        /// </summary>
        TopToBottom,

        /// <summary>
        /// BottomToTop enum of the animation direction is allows the animation from bottom to top.
        /// </summary>
        BottomToTop
    }

    /// <summary>
    /// Defines the shape for the Shimmer view. 
    /// </summary>
    public enum ShimmerShapeType
    {
        /// <summary>
        /// Circle shape of shimmer view.
        /// </summary>
        Circle,

        /// <summary>
        /// Rectangle shape of shimmer view.
        /// </summary>
        Rectangle,

        /// <summary>
        /// Rounded rectangle shape of shimmer view.
        /// </summary>
        RoundedRectangle
    }
}
